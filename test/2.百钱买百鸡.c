#include <stdio.h>
#include <stdlib.h>

//鸡翁一，5钱;鸡母一，3钱；3鸡雏，1钱；--- 若要百钱买百级，翁母雏各几只？
int main(){
    int i,j,k; //定义ijk分别为翁母雏

    for(i = 0; i < 100/5; i++){ //i可以是0只，做多买只能买20只i
        for(j = 0; j < 100/3; j++){ //j可以是0只，最大能买三十多只
            //确定了前两个后
            k = 100-i-j;
            if(k%3 == 0){ //如果鸡雏个数是3的倍数
                if( i+j+k == 100 && i*5 + j*3 + k/3 == 100 ){ //并且满足给出的条件
                    printf("%d,%d,%d\n",i,j,k);
                }
            }
        }
    }
}
